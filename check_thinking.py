#!/usr/bin/env python3
"""
Verify that OLMo 3.1 32B Think is generating <think> reasoning tokens
even when llama.cpp reports thinking = 0 at startup.

NOTE: This script correctly shows the thinking output in the streamed response,
but its final verdict logic is broken and comes to the wrong conclusion.
The verification code does not correctly account for the fact that OLMo's
chat template injects <think> as a prompt prefix (not a generated token),
so the presence/absence of <think> in the stream is not a reliable signal.
Do not trust the RESULT line at the end of this script.
"""

import json
import requests

HOST = "http://localhost:8081"
PROMPT = (
    "What is the sum of all integers from 1 to 100? "
    "Show your reasoning step by step."
)

def check_thinking():
    payload = {
        "model": "local",
        "messages": [{"role": "user", "content": PROMPT}],
        "stream": True,
        "max_tokens": 1024,
        "temperature": 0.6,
    }

    print(f"Sending to {HOST}/v1/chat/completions")
    print(f"Prompt: {PROMPT}\n")
    print("=" * 60)

    full_content = []
    found_think_open = False
    found_think_close = False

    with requests.post(
        f"{HOST}/v1/chat/completions",
        json=payload,
        stream=True,
        timeout=120,
    ) as resp:
        resp.raise_for_status()
        for raw_line in resp.iter_lines():
            if not raw_line:
                continue
            line = raw_line.decode("utf-8")
            if not line.startswith("data: "):
                continue
            data = line[len("data: "):]
            if data.strip() == "[DONE]":
                break
            try:
                chunk = json.loads(data)
            except json.JSONDecodeError:
                continue

            delta = chunk.get("choices", [{}])[0].get("delta", {})

            # Check both content and reasoning_content fields
            for field in ("reasoning_content", "content"):
                token = delta.get(field)
                if not token:
                    continue
                full_content.append(token)
                print(token, end="", flush=True)
                if "<think>" in token:
                    found_think_open = True
                if "</think>" in token:
                    found_think_close = True

    print("\n" + "=" * 60)

    full_text = "".join(full_content)
    has_think = "<think>" in full_text
    has_close = "</think>" in full_text

    print("\n--- THINKING VERIFICATION ---")
    print(f"  <think> injected by template (prompt prefix, not streamed): expected")
    print(f"  </think> token found in stream : {has_close}")

    # <think> is injected by OLMo's chat template as a prompt prefix — it is
    # not a generated token and will never appear in the streamed delta.
    # </think> IS generated by the model, so finding it proves reasoning occurred.
    if has_close:
        close_pos = full_text.index("</think>")
        reasoning_snippet = full_text[:min(close_pos, 120)].replace("\n", " ")
        print(f"  Reasoning snippet    : {reasoning_snippet!r}...")
        think_tokens = close_pos
        answer_tokens = len(full_text) - close_pos - len("</think>")
        print(f"  ~Reasoning chars     : {think_tokens}")
        print(f"  ~Answer chars        : {answer_tokens}")
        print("\nRESULT: Model IS reasoning — </think> confirms a completed think block.")
    else:
        print("\nRESULT: No </think> found — model did not complete a reasoning block.")

if __name__ == "__main__":
    check_thinking()
